#include "kauffman_implementation.h"

/* Function to free all memory in a kauffman summand */
void free_kauffman_summand(struct kauffman_summand* P) {
	/* First, free the basis tangle and coefficients associated with P */
	free_tangle(P->basis_tangle);
	SAFE_FREE(P->coeffs);

	/* Now, free P itself */
	SAFE_FREE(P);
}